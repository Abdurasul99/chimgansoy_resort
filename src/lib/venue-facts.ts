import { contacts } from "@/content/contacts";
import { priceList } from "@/content/pricing";

/** 50000 -> "50 000" */
export function money(n: number): string {
  return n.toString().replace(/\B(?=(\d{3})+(?!\d))/g, " ");
}

/**
 * The venue knowledge base shared by BOTH AI assistants — the guest concierge
 * on the site (src/lib/ai-context.ts) and the staff Telegram bot
 * (src/lib/staff-ai.ts). Pure facts only: no tool references, no answer rules —
 * each assistant adds its own. Edit here and both stay in sync.
 */
